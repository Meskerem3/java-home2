
public class Student {
    private int id;
    private String name;
    private int[] grade;

    public Student(int id, String name, int[] grade) {
        this.id = id;
        this.name = name;
        this.grade = grade;
    }

    // Getter and Setter methods for id
    public int getId() {
        return this.id;
    }




    // Getter and Setter methods for name
    public String getName() {
        return this.name;
    }



    // Getter and Setter methods for gpa
    public int[]getGrade() {
        return this.grade;
    }


    }

